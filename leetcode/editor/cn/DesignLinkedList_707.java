    class MyLinkedList {

        public static class Node {
            private Node next;
            private int val;

            public Node() {

            }

            public Node(int value) {
                this.val = value;
            }
        }

        private Node dummyHead;
        private Node tail;

        public MyLinkedList() {
            dummyHead = new Node();
            tail = dummyHead;
        }

        public int get(int index) {
            Node current = dummyHead;
            while (index >= 0) {
                if (current.next == null) {
                    return -1;
                } else {
                    current = current.next;
                    index--;
                }
            }
            return current.val;
        }

        public void addAtHead(int val) {
            Node insert = new Node(val);
            insert.next = dummyHead.next;
            dummyHead.next = insert;
            if (tail.next != null) {
                tail = tail.next;
            }
        }

        public void addAtTail(int val) {
            tail.next = new Node(val);
            tail = tail.next;
        }

        public void addAtIndex(int index, int val) {
            Node current = dummyHead;
            while (index > 0) {
                if (current.next == null) {
                    return;
                } else {
                    current = current.next;
                    index--;
                }
            }

            Node insert = new Node(val);
            insert.next = current.next;
            current.next = insert;

            if (tail.next != null) {
                tail = tail.next;
            }
        }

        public void deleteAtIndex(int index) {
            if (dummyHead.next == null) {
                return;
            }

            Node pre = dummyHead;
            while (index > 0) {
                if (pre.next == null) {
                    return;
                } else {
                    pre = pre.next;
                    index--;
                }
            }
            if (pre.next != null) {
                if (pre.next == tail) {
                    tail = pre;
                    pre.next = null;
                } else {
                    pre.next = pre.next.next;
                }
            }
        }
    }
